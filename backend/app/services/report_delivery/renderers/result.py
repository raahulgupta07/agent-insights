"""Mode A — single query result: clean table + sanitized intro + insights + SQL.

This is the renderer that fixes the raw-chat-dump bug: the table comes from the
structured ``steps.data`` (accurate), and the agent prose is sanitized down to an
intro paragraph + insight bullets (no planning markers, no "see table above", no
tool code, no fuzzy recap table).
"""
from __future__ import annotations

import logging

from app.services.report_delivery.contract import (
    DeliveryContext,
    DeliveryParts,
    register_renderer,
)
from app.services.report_delivery import extract, template

logger = logging.getLogger(__name__)


async def render(ctx: DeliveryContext) -> DeliveryParts:
    raw = await extract.latest_narrative(ctx.report_id)
    narrative = extract.sanitize_chat_content(raw)
    intro, insights = extract.split_intro_and_insights(narrative)
    result = await extract.extract_result(ctx.report_id)

    # graceful fallbacks ------------------------------------------------------
    tbl = template.table_html(result) if result else ""
    if not insights and result and result.get("rows"):
        insights = _insights_from_rows(result)
    if not intro:
        intro = "Automated report generated by this agent."

    inner = (
        f"<p style='font-size:14px;margin:0 0 4px'>{intro}</p>"
        + (f"<h3 style='margin:20px 0 8px;font-size:15px;font-family:Segoe UI,Arial,sans-serif'>Result</h3>{tbl}" if tbl else "")
        + template.insights_html(insights)
        + template.sql_block(result.get("sql") if result else None)
    )

    title = ctx.title or "Report"
    meta = "Automated analysis · agent report"
    footer = "Generated automatically by this agent and delivered via its configured email."
    body = template.skeleton(
        title=title, meta=meta, inner_html=inner, report_url=ctx.report_url, footer=footer
    )
    return DeliveryParts(body_html=body, subject=title)


def _insights_from_rows(result: dict) -> list[tuple[str, str]]:
    """Compute a couple of data-derived insights when the agent gave no prose
    (so a report is never insight-less)."""
    rows = result["rows"]
    cols = result.get("columns") or (list(rows[0].keys()) if isinstance(rows[0], dict) else [])
    if not rows or not isinstance(rows[0], dict):
        return [("Rows", f"{len(rows)} rows returned.")]
    # find a numeric measure column
    from app.services.report_delivery.template import _is_num_col
    measure = next((c for c in cols if _is_num_col(c)), None)
    label = next((c for c in cols if not _is_num_col(c)), cols[0] if cols else None)
    out: list[tuple[str, str]] = []
    if measure and label:
        try:
            top = rows[0]
            out.append(("Leader", f"<b>{top.get(label)}</b> tops the result at {top.get(measure)}."))
            if len(rows) > 1:
                lo = rows[-1]
                out.append(("Spread", f"Ranges from {lo.get(measure)} ({lo.get(label)}) to {top.get(measure)} across {len(rows)} rows."))
        except Exception:  # noqa: BLE001
            pass
    if not out:
        out.append(("Summary", f"{len(rows)} rows across {len(cols)} columns."))
    return out


register_renderer("result", render)
